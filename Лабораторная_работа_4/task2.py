def get_percent_char(dict_):  # ФУНКЦИЯ КОТОРАЯ СЧИТАЕТ ПРОЦЕНТНОЕ СОДЕРЖАНИЕ БУКВ
    sum_count = sum(dict_.values())  # сумма всех значений
    for symbol, count in dict_.items():
        dict_[symbol] = (count / sum_count) * 100
        # замена значений на процентное содержание (100 для перевода в проценты)

    return dict_
