def get_count_char(str_):  # ФУНКЦИЯ ДЛЯ СОЗДАНИЯ СЛОВАРЯ БУКВ
    str_ = str_.split(" ")  # разделям и в качестве разделителя используем пробел
    str_ = "".join(str_)  # склеиваем слова
    str_ = str_.lower()  # переводим строку в нижний регистр

    main_dict = {}  # создаем пустой словарь
    def_count = 0  # задаем значение по умолчанию

    for letter in str_:  # перебор символов строки
        if letter.isalpha():  # проверка, является ли символ буквой
            main_dict[letter] = main_dict.get(letter, def_count) + 1  # подсчет букв

    return main_dict


